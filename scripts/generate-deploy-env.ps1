# Generate all local deploy env files in the correct places (secrets stay local, not committed).
# Usage:
#   powershell -ExecutionPolicy Bypass -File scripts\generate-deploy-env.ps1
#   powershell -ExecutionPolicy Bypass -File scripts\generate-deploy-env.ps1 -VercelHost "https://xxx.vercel.app"
#   powershell -ExecutionPolicy Bypass -File scripts\generate-deploy-env.ps1 -Force

param(
  [string]$VercelHost = "",
  [string]$ApiPublicBase = "",
  [switch]$Force
)

$ErrorActionPreference = "Stop"
$Root = Split-Path -Parent $PSScriptRoot
$Backend = Join-Path $Root "backend"
$Frontend = Join-Path $Root "frontend"

function Read-EnvFile([string]$Path) {
  $map = @{}
  if (-not (Test-Path $Path)) { return $map }
  Get-Content $Path -Encoding UTF8 | ForEach-Object {
    $line = $_.Trim()
    if (-not $line -or $line.StartsWith("#") -or $line -notmatch "=") { return }
    $i = $line.IndexOf("=")
    $map[$line.Substring(0, $i).Trim()] = $line.Substring($i + 1)
  }
  return $map
}

function New-Secret([int]$Len = 48) {
  try {
    return (& python -c "import secrets; print(secrets.token_urlsafe($Len))" 2>$null)
  } catch {}
  $bytes = New-Object byte[] $Len
  [System.Security.Cryptography.RandomNumberGenerator]::Create().GetBytes($bytes)
  return [Convert]::ToBase64String($bytes).Replace("+", "").Replace("/", "").Replace("=", "").Substring(0, [Math]::Min($Len, 64))
}

function Write-Utf8NoBom([string]$Path, [string]$Content) {
  $utf8 = New-Object System.Text.UTF8Encoding $false
  [System.IO.File]::WriteAllText($Path, $Content.TrimEnd() + "`n", $utf8)
  Write-Host "  wrote $Path"
}

function Should-Write([string]$Path) {
  if ($Force) { return $true }
  if (-not (Test-Path $Path)) { return $true }
  Write-Host "  skip (exists): $Path  (use -Force to overwrite)"
  return $false
}

# Merge secrets from existing files
$fromLocal = Read-EnvFile (Join-Path $Backend ".env")
$fromDocker = Read-EnvFile (Join-Path $Backend ".env.docker")
$fromVercel = Read-EnvFile (Join-Path $Backend ".env.vercel-postgres")

function Pick([string]$Key, [string]$Fallback = "") {
  foreach ($m in @($fromDocker, $fromLocal, $fromVercel)) {
    if ($m.ContainsKey($Key) -and $m[$Key] -and $m[$Key] -notmatch "your-key|请替换|change-me|sk-your") {
      return $m[$Key]
    }
  }
  return $Fallback
}

$secretKey = Pick "SECRET_KEY" (New-Secret 48)
$dbPass = Pick "POSTGRES_PASSWORD" (New-Secret 24)
$flowerPass = Pick "FLOWER_PASSWORD" (New-Secret 16)
$openai = Pick "OPENAI_API_KEY" ""
$openaiBase = Pick "OPENAI_BASE_URL" "https://api.openai.com/v1"
$dbUrlDocker = "postgresql+asyncpg://agentflow:${dbPass}@postgres:5432/agentflow_eval"

$corsList = @(
  "http://localhost",
  "http://localhost:80",
  "http://127.0.0.1",
  "http://127.0.0.1:80",
  "http://localhost:5173",
  "http://127.0.0.1:5173"
)
if ($VercelHost) {
  $vh = $VercelHost.TrimEnd("/")
  if ($vh -notmatch "^https?://") { $vh = "https://$vh" }
  $corsList += $vh
}
$corsJson = "[" + (($corsList | ForEach-Object { "`"$_`"" }) -join ",") + "]"

Write-Host "==> Generating deploy env files under $Root"

# ---- backend/.env.docker (Docker Compose) ----
$dockerPath = Join-Path $Backend ".env.docker"
if (Should-Write $dockerPath) {
  Write-Utf8NoBom $dockerPath @"
# Auto-generated by scripts/generate-deploy-env.ps1 — DO NOT COMMIT
# Used by: docker compose --env-file .env.docker

ENV=prod
DEBUG=false
SECRET_KEY=$secretKey

CORS_ORIGINS=$corsJson

POSTGRES_USER=agentflow
POSTGRES_PASSWORD=$dbPass
POSTGRES_DB=agentflow_eval
DATABASE_URL=$dbUrlDocker

REDIS_URL=redis://redis:6379/0
CELERY_BROKER_URL=redis://redis:6379/0
CELERY_RESULT_BACKEND=redis://redis:6379/0
CELERY_TASK_ALWAYS_EAGER=false

OPENAI_API_KEY=$openai
OPENAI_BASE_URL=$openaiBase

LOG_LEVEL=INFO
LOG_FORMAT=json
RATE_LIMIT_ENABLED=true
RATE_LIMIT_DEFAULT=100/minute

AUTH_ENABLED=false
API_KEYS=
TENANCY_ENABLED=false
ADMIN_ACTORS=admin

FLOWER_USER=admin
FLOWER_PASSWORD=$flowerPass
"@
}

# ---- backend/.env.vercel-postgres (hybrid API) ----
$vercelPgPath = Join-Path $Backend ".env.vercel-postgres"
$existingDb = Pick "DATABASE_URL" ""
# Keep Neon URL if already asyncpg remote; else placeholder
if ($existingDb -match "neon\.tech|vercel|ssl=require" -and $existingDb -notmatch "@postgres:") {
  $vercelDb = $existingDb
} else {
  $vercelDb = "postgresql+asyncpg://USER:PASSWORD@HOST/neondb?ssl=require"
}
if (Should-Write $vercelPgPath) {
  $vCors = $corsJson
  Write-Utf8NoBom $vercelPgPath @"
# Auto-generated by scripts/generate-deploy-env.ps1 — DO NOT COMMIT
# Vercel Postgres / Neon + self-hosted API
# 填好 DATABASE_URL 后可用 setup-vercel-postgres.ps1 建表，或手动启动 uvicorn

ENV=prod
DEBUG=false
SECRET_KEY=$secretKey

CORS_ORIGINS=$vCors

DATABASE_URL=$vercelDb

CELERY_TASK_ALWAYS_EAGER=true
REDIS_URL=redis://localhost:6379/0
CELERY_BROKER_URL=redis://localhost:6379/0
CELERY_RESULT_BACKEND=redis://localhost:6379/0

OPENAI_API_KEY=$openai
OPENAI_BASE_URL=$openaiBase

LOG_LEVEL=INFO
LOG_FORMAT=json
AUTH_ENABLED=false
API_KEYS=
TENANCY_ENABLED=false
ADMIN_ACTORS=admin
"@
}

# ---- frontend/.env.local (Vite local) ----
$feLocal = Join-Path $Frontend ".env.local"
$apiBase = if ($ApiPublicBase) {
  $b = $ApiPublicBase.TrimEnd("/")
  if ($b -notmatch "/api/v1$") { $b = "$b/api/v1" }
  $b
} else {
  "http://127.0.0.1:8000/api/v1"
}
if (Should-Write $feLocal) {
  Write-Utf8NoBom $feLocal @"
# Auto-generated — local Vite only. DO NOT COMMIT
# Vercel: set the same key in Project → Settings → Environment Variables

VITE_API_BASE_URL=$apiBase
"@
}

# ---- frontend/.env.production.local (optional local prod build) ----
$feProdLocal = Join-Path $Frontend ".env.production.local"
if ($ApiPublicBase -and (Should-Write $feProdLocal)) {
  $b = $ApiPublicBase.TrimEnd("/")
  if ($b -notmatch "/api/v1$") { $b = "$b/api/v1" }
  Write-Utf8NoBom $feProdLocal @"
# Auto-generated for local production build tests
VITE_API_BASE_URL=$b
"@
}

# ---- ensure backend/.env exists for non-docker local ----
$envDev = Join-Path $Backend ".env"
if (-not (Test-Path $envDev)) {
  $example = Join-Path $Backend ".env.example"
  if (Test-Path $example) {
    Copy-Item $example $envDev
    Write-Host "  created backend/.env from .env.example"
  }
}

Write-Host ""
Write-Host "============================================"
Write-Host " Deploy env files ready"
Write-Host "============================================"
Write-Host " Docker:   backend\.env.docker"
Write-Host " VercelDB: backend\.env.vercel-postgres  (fill DATABASE_URL if placeholder)"
Write-Host " Frontend: frontend\.env.local"
Write-Host ""
if (-not $openai) {
  Write-Host " WARNING: OPENAI_API_KEY empty — edit backend\.env.docker and backend\.env"
}
if ($vercelDb -match "USER:PASSWORD@HOST") {
  Write-Host " NOTE: backend\.env.vercel-postgres DATABASE_URL still placeholder"
  Write-Host "       After creating Vercel Postgres, run:"
  Write-Host "       .\scripts\setup-vercel-postgres.ps1 -PostgresUrl `"postgres://...`""
}
Write-Host ""
Write-Host " Vercel dashboard variable (same name):"
Write-Host "   VITE_API_BASE_URL = https://YOUR_PUBLIC_API/api/v1"
Write-Host " Start Docker stack:"
Write-Host "   powershell -File scripts\docker-up.ps1"
Write-Host "============================================"
